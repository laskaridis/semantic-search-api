#!/bin/bash

HOST="http://localhost:8000"

create_collection() {
  if [ -z "$1" ]; then
    echo "Usage: create_collection <collection_name>" 
    exit 1
  fi

  collection=$1

  echo "Creating collection $collection"
  curl -s -o /dev/null -w "%{http_code}\\n" -X POST "$HOST/collections/$collection"
}

insert_item() {
  if [ -z "$1" ] || [ -z "$2" ]; then
    echo "Usage: insert_item <collection_name> <text>"
    exit 1
  fi

  collection=$1
  text="$2"
  id=$(uuidgen)

  echo "Inserting item with ID $id"
  curl -s -o /dev/null -w "%{http_code}\\n" -X POST "$HOST/index/$collection" \
    -H "Content-Type: application/json" \
    -d "{\"id\": \"$id\", \"text\": \"$text\"}"
}

